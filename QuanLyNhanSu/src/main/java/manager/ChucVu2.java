    private void loadData() {
        model.setRowCount(0);
        String sql = "SELECT * FROM ChucVu";
        try {
            st = con.createStatement();
            rs = st.executeQuery(sql);
            tblChucVu.removeAll();
            Vector data = null;
            while (rs.next()) {
                data = new Vector();
                data.add(rs.getString("MaCV"));
                data.add(rs.getString("TenChucVu"));
                data.add((int) rs.getFloat("LuongCoBan"));
                model.addRow(data);
            }
            tblChucVu.setModel(model);
            model.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

