    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        model.setRowCount(0);
        if (txtMaChucVu.getText().isEmpty() || txtChucVu.getText().isEmpty() || txtLuongCB.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống !");
        }
        try {

            ChucVu cv = new ChucVu();
            cv.setMaCV(txtMaChucVu.getText());
            cv.setTenChucVu(txtChucVu.getText());
            cv.setLuongCoBan(Float.parseFloat(txtLuongCB.getText()));
            dao.addChucVu(cv);
            JOptionPane.showMessageDialog(this, "Thêm chức vụ " + txtChucVu.getText() + " thành công");
            loadData();
            model.fireTableDataChanged();
            clear();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

