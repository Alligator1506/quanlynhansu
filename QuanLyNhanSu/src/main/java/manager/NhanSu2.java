    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        model.setRowCount(0);
        if (txtMNV.getText().isEmpty() || txtFullName.getText().isEmpty()
                || txtNgaySinh.getText().isEmpty() || txtPhone.getText().isEmpty()
                || txtCMT.getText().isEmpty()
                || txtNgayvaolam.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống !");
        }
        try {
            NhanVien nv = new NhanVien();
            nv.setMaNV(txtMNV.getText());
            nv.setHoTen(txtFullName.getText());
            nv.setGioiTinh(cbGioiTinh.getSelectedItem().toString());
            nv.setCmt(txtCMT.getText());
            nv.setEmail(txtMail.getText());
            try {
                nv.setNgaySinh(new SimpleDateFormat("yyyy-MM-dd").parse(txtNgaySinh.getText()));
            } catch (ParseException ex) {
                ex.printStackTrace();
            }        
            nv.setDiaChi(txtDiaChi.getText());
            try {
                nv.setNgayVaoLam(new SimpleDateFormat("yyyy-MM-dd").parse(txtNgayvaolam.getText()));
            } catch (ParseException ex) {
                ex.printStackTrace();
            }
            nv.setDienThoai(txtPhone.getText());            
            nv.setMaTD(cbTrinhDo.getSelectedItem().toString());
            nv.setMaCV(cbChucVu.getSelectedItem().toString());
            nv.setMaPB(cbPhongBan.getSelectedItem().toString());
            dao.addNhanVien(nv);
            JOptionPane.showMessageDialog(this, "Thêm nhân viên " + txtFullName.getText() + " thành công");
            loadData();
            model.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

