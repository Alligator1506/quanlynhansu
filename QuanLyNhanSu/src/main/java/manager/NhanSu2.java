    private void loadData() {
        String sql = "select * from NHANVIEN, TRINHDO, CHUCVU, PHONGBAN where NHANVIEN.MaTD=TRINHDO.MaTD and NHANVIEN.MaCV=CHUCVU.MaCV and NHANVIEN.MaPB=PHONGBAN.MaPB";
        try {
            st = con.createStatement();
            rs = st.executeQuery(sql);
            tblNhanVien.removeAll();
            String[] arr = {"Mã NV", "Tên NV", "Giới Tính", "CCCD", "Email", "Ngày Sinh", "Địa chỉ", "Ngày Vào Làm", "Điện Thoại", " Mã Trình Độ", "Mã Chức Vụ", " Mã Phòng Ban"};
            Vector data = null;
            model = new DefaultTableModel(arr, 0);
            while (rs.next()) {
                data = new Vector();
                data.add(rs.getString("MaNV"));
                data.add(rs.getString("HoTen"));
                data.add(rs.getString("GioiTinh"));
                data.add(rs.getString("CMT"));
                data.add(rs.getString("Email"));
                data.add(rs.getDate("NgaySinh"));
                data.add(rs.getString("DiaChi"));
                data.add(rs.getDate("NgayVaoLam"));
                data.add(rs.getInt("Dienthoai"));
                data.add(rs.getString("MaTD"));
                data.add(rs.getString("MaCV"));
                data.add(rs.getString("MaPB"));
                model.addRow(data);
            }
            tblNhanVien.setModel(model);
            model.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

