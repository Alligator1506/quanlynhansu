    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        cbChucVu.removeAllItems();
        cbGioiTinh.removeAllItems();
        cbPhongBan.removeAllItems();
        cbTrinhDo.removeAllItems();
        txtMNV.setText(model.getValueAt(row, 0).toString());
        txtFullName.setText(model.getValueAt(row, 1).toString());
        cbGioiTinh.addItem(model.getValueAt(row, 2).toString());
        txtCMT.setText(model.getValueAt(row, 3).toString());
        txtMail.setText(model.getValueAt(row, 4).toString());
        txtNgaySinh.setText(model.getValueAt(row, 5).toString());
        txtDiaChi.setText(model.getValueAt(row, 6).toString());
        txtNgayvaolam.setText(model.getValueAt(row, 7).toString());
        txtPhone.setText(model.getValueAt(row, 8).toString());
        cbTrinhDo.addItem(model.getValueAt(row, 9).toString());
        cbChucVu.addItem(model.getValueAt(row, 10).toString());
        cbPhongBan.addItem(model.getValueAt(row, 11).toString());
    }//GEN-LAST:event_tblNhanVienMouseClicked

