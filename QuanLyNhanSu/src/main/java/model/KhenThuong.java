public class KhenThuong {
    private String maKhenThuong, maNV, note;
    private float tienThuongPhat;
    private int maHinhThuc;

    public String getMaKhenThuong() {
        return maKhenThuong;
    }

    public void setMaKhenThuong(String maKhenThuong) {
        this.maKhenThuong = maKhenThuong;
    }

    public String getMaNV() {
        return maNV;
    }

    public void setMaNV(String maNV) {
        this.maNV = maNV;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public float getTienThuongPhat() {
        return tienThuongPhat;
    }

    public void setTienThuongPhat(float tienThuongPhat) {
        this.tienThuongPhat = tienThuongPhat;
    }

    public int getMaHinhThuc() {
        return maHinhThuc;
    }

    public void setMaHinhThuc(int maHinhThuc) {
        this.maHinhThuc = maHinhThuc;
    }
    
}
